    private void loadBioinfo(){
        
        String url = "jdbc:mysql://localhost/iuea";
        String user = "root";
        String pass = "";
        
        try{
            Connection con = DriverManager.getConnection(url, user, pass);
            
            String query = "SELECT * FROM bioinfo";
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            
            DefaultListModel id_model = new DefaultListModel();
            DefaultListModel surname_model = new DefaultListModel();
            DefaultListModel othername_model = new DefaultListModel();
            DefaultListModel sex_model = new DefaultListModel();
           //model.setRowCount(0);
            
            if(rs.isBeforeFirst()){
                while(rs.next()){
                    String strSID = rs.getString("sid");
                    String strSurname = rs.getString("surname");
                    String strOthername = rs.getString("othername");
                    String strSex = rs.getString("sex");
                    
                    id_model.addElement(strSID);
                    surname_model.addElement(strSurname);
                    othername_model.addElement(strOthername);
                    sex_model.addElement(strSex);
                    
                    
                    //String[] obj = {strSID, strSurname, strOthername, strSex};
                    //model.addRow(obj);
                    list_id.setModel(id_model);
                    list_surname.setModel(surname_model);
                    list_othername.setModel(othername_model);
                    list_sex.setModel(sex_model);
                   
                }
            }else{
                JOptionPane.showMessageDialog(this, "No Students Registered Yet!");
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
