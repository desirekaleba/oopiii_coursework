    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String strSurname = txtSurname.getText().trim();
        String strOthername = txtOthername.getText().trim();
        String strSex;
        
        if(strSurname.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter Sur Name!");
            txtSurname.requestFocus();
            return;
        }
        
        if(strOthername.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter Other Name!");
            txtOthername.requestFocus();
            return;
        }
        
        if(rdbMale.isSelected())
            strSex = "Male";
        else if(rdbFemale.isSelected())
            strSex = "Female";
        else{
            JOptionPane.showMessageDialog(this, "Please Select Sex!");
            return;
        }
           
        
        String url = "jdbc:mysql://localhost/iuea";
        String user = "root";
        String pass = "";
        
        try{
            Connection con = DriverManager.getConnection(url, user, pass);
            
            String query = "INSERT INTO bioinfo(surname, othername, sex) VALUES (?, ?, ?)";
            
            PreparedStatement pSt = con.prepareStatement(query);
            
            pSt.setString(1, strSurname);
            pSt.setString(2, strOthername);
            pSt.setString(3, strSex);
            
            pSt.execute();
            
            JOptionPane.showMessageDialog(this, "Student Successfully Saved!");
            
            txtSurname.setText("");
            txtOthername.setText("");
            rdbMale.setSelected(false);
            rdbFemale.setSelected(false);
            
            loadBioinfo();
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        
    }//GEN-LAST:event_btnSaveActionPerformed

